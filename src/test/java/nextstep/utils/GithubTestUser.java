package nextstep.utils;

public class GithubTestUser {
    private final String email;
    private final int age;

    public GithubTestUser(String email, int age) {
        this.email = email;
        this.age = age;
    }

    public String getEmail() {
        return email;
    }

    public int getAge() {
        return age;
    }
}
